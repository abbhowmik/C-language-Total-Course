#include <stdio.h>
#define PI 3.14 // it is the modern process
int main()
{
    int radious = 4;
    int height = 55;
    printf("The value of pi is %f\n", PI);
    printf("The area of the circle is %0.3f unit^2\n", PI * radious * radious);
    printf("The volume of a given cylinder is %0.2f unit^3", PI * radious * radious * height);
    return 0;
}
